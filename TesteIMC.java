    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        double peso = Double.parseDouble(this.txtPeso.getText());
        double altura = Double.parseDouble(this.txtAltura.getText());
        double imc = peso / (altura*altura);
        JOptionPane.showMessageDialog(null, "Seu Imc Ã©: " + String.format("%.1f", imc));
        
        if(imc < 18.5){
            JOptionPane.showMessageDialog(null, "Magro!");
        }else if(imc >= 18.5 && imc <= 24.9){
            JOptionPane.showMessageDialog(null, "Peso normal!");
        }else if(imc >= 25 && imc <= 29.9){
            JOptionPane.showMessageDialog(null, "Sobrepeso!");
        }else if(imc >= 30 && imc <= 34.9){
            JOptionPane.showMessageDialog(null, "Obesidade Grau 1!");
        }else if(imc >= 35 && imc <= 40){
            JOptionPane.showMessageDialog(null, "Obesidade Grau 2!");
        }else if(imc > 40){
            JOptionPane.showMessageDialog(null, "Obesidade Grau 3!");
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

